    void muestra(){
        DefaultTableModel dt = (DefaultTableModel)jTable1.getModel();
        dt.setRowCount(0);
        for(Producto p:aD.obtener()){
            Object v[] = {p.getCodigo(),p.getCantidad(),p.getPrecio(),p.getNombrePro()};
            dt.addRow(v);
        }
    }

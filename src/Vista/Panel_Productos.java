    void muestra(){
        DefaultTableModel dt = (DefaultTableModel)jTable1.getModel();
        dt.setRowCount(0);
        for (Producto p : cad_producto.obtener()) {
            Object v[] = {p.getCodigo(),p.getNombrePro(),p.getCantidad(),p.getPrecio()};
            dt.addRow(v);
        }
    }
    

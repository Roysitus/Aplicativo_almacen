    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String codigo, fecha = txtFechaIngreso.getText();
        int cantidad = Integer.parseInt(txtCantidadIngreso.getText());
        codigo = jComboBox1.getSelectedItem().toString();
        Producto p = aD.buscar(codigo);
        p.setCantidad(p.getCantidad()+cantidad);
        ingreso_productos i_p = new ingreso_productos(codigo, fecha, cantidad);
        cad_ingreso.crear(i_p);
        aD.actualizar(p);
        JOptionPane.showMessageDialog(null, "Ingreso correctamente");
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String codigo, fecha = txtFechaRetiro.getText();
        int cantidad = Integer.parseInt(txtCantidadRetiro.getText());
        codigo = jComboBox2.getSelectedItem().toString();
        Producto p = aD.buscar(codigo);
        if(cantidad>p.getCantidad()){
            JOptionPane.showMessageDialog(null, "La cantidad sobrepasa el stock");
        }else{
            p.setCantidad(p.getCantidad()-cantidad);
            retiro_productos r_p = new retiro_productos(codigo, fecha, cantidad);
            cad_retiro.crear(r_p);
            aD.actualizar(p);
            JOptionPane.showMessageDialog(null, "Retiro correctamente");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

